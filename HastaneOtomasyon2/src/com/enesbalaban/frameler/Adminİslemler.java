    private void doktorEkleButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doktorEkleButonActionPerformed

        mesajYazisi.setText("");

        ArrayList<Doktor> doktorlarTc = new ArrayList<Doktor>();

        doktorlarTc = islemler.doktorGetir();

        String ad = doktorAdAlani.getText();
        String soyad = doktorSoyadAlani.getText();
        String bolum = (String) cbDoktorBolum.getSelectedItem();
        String tc = doktorTCAlani.getText();
        String cinsiyet = (String) cbDoktorCinsiyet.getSelectedItem();
        String email = doktorEmailAlani.getText();
        String telefonNo = doktorTelefonAlan.getText();

        boolean varmi = true;

        if (!ad.equals("") && !soyad.equals("") && !email.equals("") && !telefonNo.equals("") && !tc.equals("")) {

            for (Doktor doktor : doktorlarTc) {

                if (doktor.getTcNo().equals(tc)) {

                    JOptionPane.showMessageDialog(this, "Zaten randevunuz var...");

                    varmi = false;

                    break;

                }
                if (varmi) {
                    islemler.doktorEkle(ad, soyad, bolum, tc, cinsiyet, email, telefonNo);

                    JOptionPane.showMessageDialog(this, "Randevunuz başarıyla alındı...");

                    doktorAdAlani.setText("");
                    doktorSoyadAlani.setText("");
                    doktorTCAlani.setText("");
                    doktorEmailAlani.setText("");
                    doktorTelefonAlan.setText("");

                }

            }

        } else {

            JOptionPane.showMessageDialog(this, "Lütfen tüm bilgilerinizi girin...");
        }

       


    }//GEN-LAST:event_doktorEkleButonActionPerformed

    private void doktorSilButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doktorSilButonActionPerformed
        mesajYazisi.setText("");

        int selectedRow = doktorTablosu.getSelectedRow();

        if (selectedRow == -1) {

            if (modelDoktor.getRowCount() == 0) {
                mesajYazisi.setText("Doktor tablosu şuan boş...");
            } else {
                mesajYazisi.setText("Lütfen silinecek bir doktor seçin...");
            }
        } else {

            int id = (int) modelDoktor.getValueAt(selectedRow, 0);

            islemler.doktorSil(id);

            doktorGoruntule();

            mesajYazisi.setText("Doktor başarıyla silindi...");

        }
    }//GEN-LAST:event_doktorSilButonActionPerformed

