    private void hastaGuncelleButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hastaGuncelleButonActionPerformed
        String ad = hastaAdAlani.getText();
        String soyad = hastaSoyadAlani.getText();
        String cinsiyet = (String) cbHastaCinsiyet.getSelectedItem();
        String email = hastaEmailAlani.getText();
        String telefonNo = hastaTelefonAlani.getText();
        String polikinlik = (String) cbHastaPolikinlik.getSelectedItem();
        String tc = hastaTcAlani.getText();
        String saat = (String) cbSaat.getSelectedItem();

        int selectedRow = hastaTablosu.getSelectedRow();

        if (selectedRow == -1) {

            if (modelHasta.getRowCount() == 0) {

                JOptionPane.showMessageDialog(this, "Tabloda hiç hasta yok..");
            } else {
                JOptionPane.showMessageDialog(this, "Lütfen güncellenecek bir hasta seçin...");
            }

        } else {

            int id = (int) modelHasta.getValueAt(selectedRow, 0);

            islemler.hastaGuncelle(id, ad, soyad, cinsiyet, email, telefonNo, polikinlik, tc, saat);

            hastaGoruntule();

            JOptionPane.showMessageDialog(this, "Hasta başarıyla güncellendi..");

        }
    }//GEN-LAST:event_hastaGuncelleButonActionPerformed

