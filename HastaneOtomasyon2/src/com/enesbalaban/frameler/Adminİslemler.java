    public void doktorGoruntule() {
        modelDoktor.setRowCount(0);

        ArrayList<Doktor> doktorlar = new ArrayList<Doktor>();

        doktorlar = islemler.doktorGetir();

        if (doktorlar != null) {

            for (Doktor doktor : doktorlar) {

                Object[] eklenecek = {doktor.getId(), doktor.getIsim(), doktor.getSoyisim(), doktor.getBolum(), doktor.getTcNo(), doktor.getCinsiyet(), doktor.getEmail(), doktor.getTelefonNo()};

                modelDoktor.addRow(eklenecek);

            }
        }
    }

    public void hastaGoruntule() {

        modelHasta.setRowCount(0);

        ArrayList<Hasta> hastalar = new ArrayList<Hasta>();

        hastalar = islemler.hastaGetir();

        if (hastalar != null) {

            for (Hasta hasta : hastalar) {

                Object[] eklenecek = {hasta.getId(), hasta.getIsim(), hasta.getSoyisim(), hasta.getTcNo(), hasta.getCinsiyet(), hasta.getEmail(), hasta.getTelefonNo(), hasta.getPolikinlik(), hasta.getSaat()};

                modelHasta.addRow(eklenecek);

            }
        }

    }

    public void polikinlikGoruntule() {

        modelPolikinlik.setRowCount(0);

        ArrayList<Polikinlikler> polikinlikler = new ArrayList<Polikinlikler>();

        polikinlikler = islemler.polikinlikGetir();

        if (polikinlikler != null) {

            for (Polikinlikler polikinlik : polikinlikler) {

                Object[] eklenecek = {polikinlik.getId(), polikinlik.getAd(), polikinlik.getKapasite(), polikinlik.getOdaSayisi()};

                modelPolikinlik.addRow(eklenecek);

            }
        }

    }

